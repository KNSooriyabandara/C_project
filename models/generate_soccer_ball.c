// === Triangulation Helper ===
static int triangulate_faces(
    int** polygons,
    int* face_vertex_counts,
    int face_count,
    int (**out_triangles)[3]
) {
    int total_tris = 0;
    for (int i = 0; i < face_count; i++) {
        int n = face_vertex_counts[i];
        if (n < 3) continue;
        total_tris += (n - 2);
    }

    int (*tris)[3] = malloc(sizeof(int[3]) * total_tris);
    int tri_index = 0;

    for (int i = 0; i < face_count; i++) {
        int n = face_vertex_counts[i];
        int* poly = polygons[i];
        for (int t = 0; t < n - 2; t++) {
            tris[tri_index][0] = poly[0];
            tris[tri_index][1] = poly[t + 1];
            tris[tri_index][2] = poly[t + 2];
            tri_index++;
        }
    }

    *out_triangles = tris;
    return total_tris;
}

// === Generate Soccer Ball ===
void generate_soccer_ball(mesh_t* mesh) {
    mesh->vertex_count = sizeof(vtx) / sizeof(vtx[0]);
    mesh->vertices = malloc(sizeof(vec3_t) * mesh->vertex_count);
    for (int i = 0; i < mesh->vertex_count; ++i)
        mesh->vertices[i] = vtx[i];

    mesh->edge_count = sizeof(edges) / sizeof(edges[0]);
    mesh->edges = edges; // static edges - no free!

    mesh->face_count = triangulate_faces(polygons, face_vertex_counts, 24, &mesh->faces);
}
